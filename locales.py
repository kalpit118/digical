def get_translator(lang_code):
    """
    Returns a translation function for the specified language.
    Falls back to English if the language or translation key is missing.
    """
    fallback = TRANSLATIONS.get("en", {})
    dictionary = TRANSLATIONS.get(lang_code, fallback)

    def translate(text):
        # Look up text in current language dictionary, fallback to English, or return original text
        return dictionary.get(text, fallback.get(text, text))

    return translate
